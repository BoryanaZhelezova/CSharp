using System;


class Strings_and_Objects
{
    static void Main()
    {
        string string1 = "Hello";
        string string2 = "World";


        object concatenatedString = string1 + " " + string2;
        string newString = (string)concatenatedString;

        object container1 = 5;
        object container2 = "Five";
        Console.WriteLine("BGDFUSF", container1);
        Console.WriteLine("fdsfmhbcx", container2);

        Console.WriteLine(newString);
    }
}
